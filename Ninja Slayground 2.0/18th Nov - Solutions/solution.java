public class Solution {
    public static double areaSwitchCase(int ch, double []a) {
        // Write your code here
        if(ch == 1)
            return Double.parseDouble(String.format("%.5f", (Math.PI * a[0] * a[0])));
        return Double.parseDouble(String.format("%.5f", (a[0] * a[1])));
    }
}
